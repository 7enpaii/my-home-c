int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (n && *s1 && *s1 == *s2)
	{
		n--;
		s2++;
		s1++;
	}
	if (n)
		return (*s1 - *s2);
	else
		return (0);
}

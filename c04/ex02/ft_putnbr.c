#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long int	n;

	n = nb;
	if (n < 0)
	{
		print('-');
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	print((n % 10) + '0');
}
